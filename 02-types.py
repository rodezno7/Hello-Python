# string o mejor conocido como una cadena de texto
my_name = "Bayron"
my_name = 'Alejandro'

#fijemonos que si utilizamos "" comillas dobles o comillas simples '' funciona de la misma manera
print('my_name => ', my_name)

#De esta manera podemos obtener el tipo de variable que estamos utilizando
print(type(my_name))

# int o entero --> Estos no necesitan llevar comillas
my_age = 28
print('my_age =>', my_age)
print(type(my_age))

# Boolean <<True or False>>
# Debemos tener en cuenta que el valor True o False debe llevar la primer letra mayuscula
is_single = False
print('is_single =>', is_single)
print(type(is_single))

# inputs
my_age = input('Cual es tu edad?')
print('my_age =>', my_age)
print(type(my_age))